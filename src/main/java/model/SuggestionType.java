package model;

/**
 * Enumeration representing the types of suggestions that can be made for a camp (Camp Attributes).
 * These suggestion types include NAME, START_DATE, END_DATE, CLOSING_DATE, USER_GROUP,
 * LOCATION, TOTAL_SLOTS, and DESCRIPTION.
 *
 * @author Seung Yeon
 * @version 1.0
 * @since 2023-11-14
 */
public enum SuggestionType {
    NAME,
    START_DATE,
    END_DATE,
    CLOSING_DATE,
    USER_GROUP,
    LOCATION,
    TOTAL_SLOTS,
    DESCRIPTION
}